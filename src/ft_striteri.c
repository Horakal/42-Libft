#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	index;

	index = 0;
	if (s != NULL)
	{
		while (s[index] != '\0')
		{
			(*f)(index, &s[index]);
			index++;
		}
	}
}
