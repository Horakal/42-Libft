#include "libft.h"

void	*ft_memset(void *dest,	int c, size_t len)
{
	unsigned char	*p;

	p = dest;
	while (len != 0)
	{
		*p = (unsigned char)c;
		p++;
		len--;
	}
	return (dest);
}
